--select for ID
CREATE PROCEDURE SELECT_CATEGORIES
	@CATEGORY_ID INT
AS
	SELECT CategoryID, CategoryName, Description, Picture 
	FROM Categories
	WHERE CategoryID = @CATEGORY_ID;
GO

